
package day4_primitives_operators;

public class SumOfTwoNumbers {
public static void main(String[] args) {
	

	long owed = 50000l;
	long acctual = 66000l;
	
	long calc = acctual - owed;
	
	System.out.println(calc);
	
	
	int count = 5;
	int count1 = 1;
	int sum = count + count1;
	
	
	System.out.println(count + " + " + count1 + " = " + sum);
	
	
	
}

}
