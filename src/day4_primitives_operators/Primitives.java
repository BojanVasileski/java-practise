package day4_primitives_operators;

public class Primitives {
	public static void main(String[] args) {
		byte byteValue = 127;
		short shortValue = 23000;
		int intValue = 1234543234;
		long longValue = 1239149938l;
		
		float floatValue = 12.123f;
		double doubleValue = 1231.1231;
		
		char charValue = '@';
		char charValue2 = 1;
		
		boolean booleanValue = true;
		boolean booleanValue2 = false;
		
		System.out.println("Byte is " + byteValue);
		System.out.println("Short is " + shortValue);
		System.out.println("Int is " + intValue);
		
		System.out.println("Long is " + longValue);
		System.out.println("Double is " + doubleValue);
		System.out.println("Float is " + floatValue);
		System.out.println("Char is " + charValue);
		System.out.println("Char2 is "+ charValue2);
		System.out.println("Boolean is " + booleanValue);
		System.out.println("Boolean2 is " + booleanValue2);
		
	}	

}
